package org.example.returnpairelement;

public class cons {

    public cons() {}

    public cons(String a, String b) {
        this.a = a;
        this.b = b;
    }

    private String a;

    private String b;

    public String getA() {
        return a;
    }

    public String getB() {
        return b;
    }
}
